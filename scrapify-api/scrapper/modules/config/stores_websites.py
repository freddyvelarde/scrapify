def ebay_store(product):
    return f"https://www.ebay.com/sch/i.html?_from=R40&_trksid=p2380057.m570.l1313&_nkw={product}&_sacat=0"


def alibaba_store(product):
    return f"https://www.alibaba.com/trade/search?fsb=y&IndexArea=product_en&CatId=&tab=all&SearchText={product}&isPremium=y&secondFlag=true"
